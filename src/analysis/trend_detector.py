    def _series_to_dict(self, series: pd.Series) -> Dict:
        """Convert time series to JSON-serializable dict"""
        return {
            "dates": series.index.strftime('%Y-%m-%d').tolist(),
            "values": series.round(2).values.tolist(),
            "column_name": series.name or "value",
        }