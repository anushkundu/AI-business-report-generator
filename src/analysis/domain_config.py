"""
DOMAIN CONFIGURATION REGISTRY
──────────────────────────────────────────────────────
Now includes AnalysisConfig, ChartConfig, and DomainVocabulary
for EVERY domain.

This is the SINGLE SOURCE OF TRUTH that controls:
  1. How columns are detected
  2. What KPIs are calculated
  3. How data is analyzed (COUNT vs SUM vs MEAN)
  4. What charts are generated
  5. What language the AI uses in reports

ADDING A NEW DOMAIN:
  1. Create column patterns
  2. Create KPI definitions
  3. Create AnalysisConfig (what questions to answer)
  4. Create ChartConfig (what charts to show)
  5. Create DomainVocabulary (what language to use)
  6. Bundle into DomainConfig
  7. Add to DOMAIN_REGISTRY
"""

from dataclasses import dataclass, field
from typing import Dict, List, Optional, Tuple


# ═══════════════════════════════════════════════════════════
# DATA CLASSES — Building Blocks
# ═══════════════════════════════════════════════════════════

@dataclass
class ColumnPatterns:
    """Maps metric roles → possible column name patterns"""
    patterns: Dict[str, List[str]] = field(default_factory=dict)


@dataclass
class KPIDefinition:
    """Defines a single KPI"""
    name: str
    key: str
    description: str
    format: str = "{:,.2f}"
    icon: str = "📊"
    requires: List[str] = field(default_factory=list)


@dataclass
class CrossTabDefinition:
    """
    Defines a cross-tabulation analysis.

    Example for Healthcare:
        row = "gender"
        col = "diagnosis"
        value = "patient_id"
        aggregation = "count"
        title = "Patient Count by Gender and Diagnosis"

    This produces a table like:
                    Diabetes  Hypertension  Fracture
        Male           45          62          28
        Female         52          58          31
    """
    row: str                    # column pattern role for rows
    col: str                    # column pattern role for columns
    value: str                  # column pattern role for values
    aggregation: str            # "count", "sum", "mean"
    title: str                  # display title
    description: str = ""       # what insight this reveals


@dataclass
class GroupByAnalysis:
    """
    Defines a group-by analysis.

    Instead of always doing SUM(revenue) GROUP BY category,
    this tells the system EXACTLY how to aggregate.

    Example for Healthcare:
        group_by_role = "diagnosis"
        metric_role = "patient"
        aggregation = "count"
        title = "Patient Count by Diagnosis"
        sort_by = "count"
        show_percentage = True

    Example for HR:
        group_by_role = "department"
        metric_role = "compensation"
        aggregation = "mean"
        title = "Average Salary by Department"
        sort_by = "mean"
        show_percentage = False
    """
    group_by_role: str          # column pattern role to group by
    metric_role: str            # column pattern role to aggregate
    aggregation: str            # "count", "sum", "mean", "median"
    title: str                  # display title
    sort_by: str = "desc"       # "desc" or "asc"
    show_percentage: bool = True
    description: str = ""


@dataclass
class DomainChartSpec:
    """
    Defines a specific chart for this domain.

    Instead of auto-guessing chart types, each domain
    EXPLICITLY defines what charts are meaningful.
    """
    chart_type: str             # "bar", "pie", "histogram", "box", "grouped_bar", "scatter"
    title: str
    x_role: str                 # column pattern role for x-axis
    y_role: Optional[str]       # column pattern role for y-axis (None for count-based)
    color_role: Optional[str] = None    # column pattern role for color grouping
    aggregation: str = "sum"    # "count", "sum", "mean", "median", "raw"
    priority: int = 5           # 1=most important
    reason: str = ""            # why this chart matters
    section: str = "general"    # report section
    max_categories: int = 15    # limit categories shown


@dataclass
class DomainVocabulary:
    """
    Domain-specific language for reports.

    Controls how the AI narrator speaks about the data.
    Prevents calling patients "customers" or
    calling salary "revenue".
    """
    entity_name: str            # "patients", "employees", "transactions"
    entity_plural: str          # "patients", "employees", "orders"
    primary_metric_name: str    # "patient count", "headcount", "revenue"
    primary_metric_verb: str    # "admitted", "employed", "generated"

    # Words to USE in this domain
    preferred_terms: List[str] = field(default_factory=list)

    # Words to NEVER USE in this domain
    forbidden_terms: List[str] = field(default_factory=list)

    # How to describe good performance
    positive_descriptors: List[str] = field(default_factory=list)

    # How to describe bad performance
    negative_descriptors: List[str] = field(default_factory=list)

    # Domain-specific insight prompts
    insight_questions: List[str] = field(default_factory=list)


@dataclass
class AnalysisConfig:
    """
    MASTER ANALYSIS CONFIGURATION for a domain.

    This is the "recipe" that tells descriptive.py:
    - What is the primary thing to count/sum/average?
    - What columns to group by?
    - What cross-tabs to compute?
    - What distributions to analyze?
    - What comparisons make sense?
    """

    # Primary metric: what is THE main thing we're measuring?
    primary_metric_role: str            # e.g., "revenue", "patient", "employee_id"
    primary_aggregation: str            # "sum", "count", "mean"

    # Secondary metrics
    secondary_metrics: List[Dict] = field(default_factory=list)
    # Each dict: {"role": "cost", "aggregation": "sum", "label": "Total Cost"}

    # Group-by analyses to perform
    group_by_analyses: List[GroupByAnalysis] = field(default_factory=list)

    # Cross-tabulations
    cross_tabs: List[CrossTabDefinition] = field(default_factory=list)

    # Distribution analyses (which numeric columns to show histograms for)
    distribution_roles: List[str] = field(default_factory=list)

    # Correlation pairs to check (which role pairs to correlate)
    correlation_pairs: List[Tuple[str, str]] = field(default_factory=list)

    # Columns to NEVER group by (high cardinality identifiers)
    exclude_from_groupby: List[str] = field(default_factory=list)

    # How to define "top/bottom performers" in this domain
    performer_metric_role: str = ""         # what metric defines performance
    performer_aggregation: str = "sum"      # how to aggregate for ranking
    performer_label: str = "Top Performers" # display label


@dataclass
class DomainConfig:
    """Complete configuration for a business domain"""
    domain_id: str
    display_name: str
    description: str
    icon: str
    column_patterns: ColumnPatterns
    kpi_definitions: List[KPIDefinition]
    analysis_config: AnalysisConfig
    chart_specs: List[DomainChartSpec]
    vocabulary: DomainVocabulary
    llm_context: str
    category_columns_hints: List[str]


