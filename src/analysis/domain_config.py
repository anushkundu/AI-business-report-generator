def get_domain_options() -> Dict[str, str]:
    """Returns dict for Streamlit dropdown"""
    options = {"🔍 Auto Detect": "auto"}
    for domain_id, config in DOMAIN_REGISTRY.items():
        if config is not None:
            options[f"{config.icon} {config.display_name}"] = domain_id
    return options


def get_domain_config(domain_id: str) -> Optional[DomainConfig]:
    """Get config for a specific domain"""
    return DOMAIN_REGISTRY.get(domain_id)